/**
 * 
 * Gives the possibility to specify his agreement to receive general and/or personalized promotions
 * (birthday special offers, new meals alert, ...).
 * 
 * Yes : all notifications are accepted
 * No : the client will never get a notification from the Enjoy Your Meal System
 * BirthdayOfferOnly : the birthday offers will be sent, but not the special offers
 * SpecialOffersOnly : the special offers will be sent, but not the birthday offers
 * 
 * @author Achraf Gharbi
 * 
 * @author Younes Laaboudi
 *
 */
public enum AgreementType {Yes, No, BirthdayOfferOnly, SpecialOffersOnly}
